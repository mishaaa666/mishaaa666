package firstLesson;

public class Switch {

    public static void main(String[] args) {

        int month = 10;

        switch (month) {
            case 1 :
                System.out.println("Case1");
                break;
            case 2 :
                System.out.println("Case2");
                break;
            case 3 :
                System.out.println("Case3");
                break;
            case 4 :
                System.out.println("Case4");
                break;
                default:
                System.out.println("Default");
        }
    }
}
